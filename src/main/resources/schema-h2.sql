CREATE TABLE AUTHOR
(
    ID         VARCHAR2(50) PRIMARY KEY,
    FIRST_NAME VARCHAR2(1000),
    LAST_NAME  VARCHAR2(1000)
);

CREATE TABLE BOOK
(
    ID         VARCHAR2(50) PRIMARY KEY,
    NAME       VARCHAR2(1000),
    PAGE_COUNT INTEGER,
    AUTHOR_ID  VARCHAR2(50)
);

ALTER TABLE BOOK ADD FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHOR(ID);
